     */
    public function store(Request $request)
    {
        $newCategory = new Category();
        $newCategory->name = $request->category;
        $newCategory->image = $request->image;
        $newCategory->res_id = Auth::user()->res_id;
        $newCategory->save();
        return
            redirect('home');
    }
